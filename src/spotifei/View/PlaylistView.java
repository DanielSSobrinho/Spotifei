    private void bt_ver_musicasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_ver_musicasActionPerformed
        int linha = tabela_playlists.getSelectedRow();
    if (linha != -1) {
        int idPlaylist = (int) tabela_playlists.getValueAt(linha, 0);
        MusicasDaPlaylistView tela = new MusicasDaPlaylistView(idPlaylist);
        tela.setVisible(true);
    } else {
        JOptionPane.showMessageDialog(this, "Selecione uma playlist para visualizar as músicas.");
    }

    }//GEN-LAST:event_bt_ver_musicasActionPerformed

    private void bt_criar_playlistActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_criar_playlistActionPerformed
        String nome = txt_nome_playlist.getText().trim();
        if (!nome.isEmpty()) {
            PlaylistDAO dao = new PlaylistDAO();
            dao.criarPlaylist(nome, idUsuario);
            atualizarTabela();
            txt_nome_playlist.setText("");
        } else {
            JOptionPane.showMessageDialog(this, "Digite um nome para a playlist.");
        }

    }//GEN-LAST:event_bt_criar_playlistActionPerformed

    private void bt_excluir_playlistActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_excluir_playlistActionPerformed
    int linha = tabela_playlists.getSelectedRow();
    if (linha != -1) {
        int idPlaylist = (int) tabela_playlists.getValueAt(linha, 0);
        String nomePlaylist = (String) tabela_playlists.getValueAt(linha, 1);

        int resposta = JOptionPane.showConfirmDialog(this,
            "Deseja excluir a playlist \"" + nomePlaylist + "\"?",
            "Confirmação",
            JOptionPane.YES_NO_OPTION);

        if (resposta == JOptionPane.YES_OPTION) {
            PlaylistDAO dao = new PlaylistDAO();
            dao.excluirPlaylist(idPlaylist);
            atualizarTabela();
        }
    } else {
        JOptionPane.showMessageDialog(this, "Selecione uma playlist para excluir.");
    }
    }//GEN-LAST:event_bt_excluir_playlistActionPerformed

