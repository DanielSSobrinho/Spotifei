    public void atualizarTabela() {
        PlaylistDAO dao = new PlaylistDAO();
        List<Playlist> playlists = dao.listarPlaylistsDoUsuario(idUsuario);

        DefaultTableModel model = new DefaultTableModel(new Object[]{"ID", "Nome"}, 0);
        for (Playlist p : playlists) {
            model.addRow(new Object[]{p.getId(), p.getNome()});
        }

        tabela_playlists.setModel(model);
    }
    
    
